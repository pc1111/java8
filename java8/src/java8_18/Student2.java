package java8_18;

public class Student2 {
	private String name;
	private String sex;
	private int score;
	
	public Student2(String name, String sex, int score) {
		this.name =name;
		this.sex = sex;
		this.score =score;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}
	
}
