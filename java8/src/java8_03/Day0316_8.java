public class Day0316_8 {
	public static void main(String[] args) {
		int a = 10;
		System.out.println(a);
		a+=1;
		System.out.println(a);
		a+=1;
		System.out.println(a);
		a+=1;
		System.out.println(a);
		
		
		a++;
		System.out.println(a);
		a++;
		System.out.println(a);
		a++;
		System.out.println(a);

		++a;
		System.out.println(a);
		++a;
		System.out.println(a);
		++a;
		System.out.println(a);
		
		a--;
		System.out.println(a);
		a--;
		System.out.println(a);
		a--;
		System.out.println(a);

		--a;
		System.out.println(a);
		--a;
		System.out.println(a);
		--a;
		System.out.println(a);
		System.out.println();
		
		a=10;
		System.out.println(++a);
		System.out.println(++a);
		System.out.println(++a);
		System.out.println(++a);
		System.out.println(++a);
		System.out.println();
		
		a=10;
		System.out.println(a++);
		System.out.println(a++);
		System.out.println(a++);
		System.out.println(a++);
		System.out.println(a++);
	}
	
}
