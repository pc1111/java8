package java8_03;

public class Day0316_7 {

	public static void main(String[] args) {
		int a = 10, b = 3;
		a += b;
		System.out.println(a);
		a -= b;
		System.out.println(a);
		a *= b;
		System.out.println(a);
		
		a = 10;
		System.out.println(a);
		a *= 3 + 10;
		System.out.println(a);
		
		a += 1;
		System.out.println(a);
		a += 1;
		System.out.println(a);
	}

}
