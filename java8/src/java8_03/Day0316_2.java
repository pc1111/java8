package java8_03;

public class Day0316_2 {

	public static void main(String[] args) {
		/*
		 * 기본 자료형은 각각을 정희하는 클래스가 존재한다.
		 * byte -> Byte
		 * short -> Short
		 * int -> Integer
		 * long -> Long
		 * float-> Float
		 * double -> Double
		 * char -> Character
		 * 
		 * 각각의 클래스에는 상수로 MAX_VALUE와 MIN_VALUE가 존재한다.
		 */
		
		System.out.println("int형의 최대값;" + Integer.MAX_VALUE);
		System.out.println("int형의 최소값;" + Integer.MIN_VALUE);
		System.out.println("byte형의 최대값;" + Byte.MAX_VALUE);
		System.out.println("byte형의 최소값;" + Byte.MIN_VALUE);


	}

}
